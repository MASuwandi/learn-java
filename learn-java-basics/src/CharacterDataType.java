public class CharacterDataType {
    public static void main(String[] args) {

        // Can only contain one char
        char t = 'T';
        char h = 'H';
        char o = 'O';
        char r = 'R';

        // print w/o new line
        System.out.print(t);
        System.out.print(h);
        System.out.print(o);

        System.out.println(r);

        // println w new line at the end
        System.out.println(t);
        System.out.println(h);
        System.out.println(o);
        System.out.println(r);
    }
}
